    [Benchmark]
    public bool HasPairSortedOnly(){

        int sm = 0, lo = 0, hi = Data!.Length - 1;

        Start();

        while (lo < hi) {

            sm = Data[lo] + Data[hi];

            if (sm == Sum){
                Stop();
                return true;
            } else if (sm < Sum) lo++;
              else if (sm > Sum) hi--;

        }

        Stop();

        return false;
    }

    [Benchmark]
    public bool HasPairHashSet() {

        HashSet<int> comp = new();

        Start();

        foreach (int value in Data!){
            if (comp.Contains(value)){
                Stop();
                return true;
            }
            comp.Add(Sum - value);
        }

        Stop();

        return false;

    }

